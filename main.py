from flask import Flask, render_template_string, request, redirect, url_for, session

app = Flask(__name__)
app.secret_key = 'your_secret_key'  # Replace with a secure key

# Dummy user data for demonstration
users = {
    'admin': 'password123',
    'user': 'userpass'
}

